using Microsoft.AspNetCore.Mvc;
using System.Diagnostics;
using WebApplication10000.DBLayer;
using WebApplicationDBService100000.Models;

namespace WebApplicationDBService100000.Controllers
{
    public class HomeController : Controller
    {
        private readonly ILogger<HomeController> _logger;
        private readonly IConfiguration _configuration;

        public HomeController(ILogger<HomeController> logger, IConfiguration configuration)
        {
            _logger = logger;
            _configuration = configuration;
        }

        public IActionResult Index()
        {
            CourseRepository courseRepository = new CourseRepository(_configuration);
            var courses = courseRepository.GetAllCourses();
            return View(courses);
        }

        public IActionResult Privacy()
        {
            return View();
        }

        [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
        public IActionResult Error()
        {
            return View(new ErrorViewModel { RequestId = Activity.Current?.Id ?? HttpContext.TraceIdentifier });
        }
    }
}
